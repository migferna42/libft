#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int ret;

	ret = -1;
	if (s)
	{
		while (*s && ++ret >= 0)
			ret = ft_putchar_fd(*s++, fd);
	}
	return (ret);
}
